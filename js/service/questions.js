var legalQuestions = []
var ilegalQuestions = []


function loadQuestions() {
	for (var i = 1; i < data.length; i++) {
		question = data[i]
		var q = { 'assertion':question['AFIRMACIÓN'], 
					'answer':question['RESPUESTA'],
					'info':question['MAS INFO'], 
					'category':question['CATEGORIA']}
		if (q['category'] == 'LEGAL') {
			legalQuestions.push(q)	
		} else {
	  		ilegalQuestions.push(q)
		}
	}
	
}

